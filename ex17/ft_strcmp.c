int	ft_strcmp(char *s1, char *s2)
{
	int	s1_pos;
	int	s2_pos;

	s1_pos = 0;
	s2_pos = 0;
	while (s1[s1_pos] != 0 || s2[s2_pos] != 0)
	{
		if (s1[s1_pos] != s2[s2_pos])
		{
			return (s1[s1_pos] - s2[s2_pos]);
		}
		s1_pos++;
		s2_pos++;
	}
	return (0);
}
