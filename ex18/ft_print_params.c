void	ft_putchar(char c);

int	main(int args, char *arg[])
{
	int	i;

	i = 1;
	while (i < args)
	{
		while (*arg[i])
		{
			ft_putchar(*arg[i]);
			arg[i]++;
		}
		ft_putchar('\n');
		i++;
	}
}
