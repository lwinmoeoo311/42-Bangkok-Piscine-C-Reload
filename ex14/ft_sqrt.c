int	ft_sqrt(int nb)
{
	int	count;
	int	num;
	int	odd;

	count = 0;
	num = nb;
	odd = 1;
	while (num > 0)
	{
		if (num == odd)
		{
			return (count + 1);
		}
		num -= odd;
		odd += 2;
		count++;
	}
	return (0);
}
